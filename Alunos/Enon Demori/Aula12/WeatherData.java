package enon;

public class WeatherData {

    private double temperaturaAtual;
    private double temperaturaMaxima;
    private double temperaturaMinima;
    private double umidade;
    private double precipitacao;
    private String condicao;
    private double ventoVelocidade;
    private String ventoDirecao;
    private String localCompleto;

    public String getLocalCompleto() {
        return localCompleto;
    }

    public void setLocalCompleto(String localCompleto) {
        this.localCompleto = localCompleto;
    }

    public double getTemperaturaAtual() {
        return temperaturaAtual;
    }

    public void setTemperaturaAtual(double temperaturaAtual) {
        this.temperaturaAtual = temperaturaAtual;
    }

    public double getTemperaturaMaxima() {
        return temperaturaMaxima;
    }

    public void setTemperaturaMaxima(double temperaturaMaxima) {
        this.temperaturaMaxima = temperaturaMaxima;
    }

    public double getTemperaturaMinima() {
        return temperaturaMinima;
    }

    public void setTemperaturaMinima(double temperaturaMinima) {
        this.temperaturaMinima = temperaturaMinima;
    }

    public double getUmidade() {
        return umidade;
    }

    public void setUmidade(double umidade) {
        this.umidade = umidade;
    }

    public double getPrecipitacao() {
        return precipitacao;
    }

    public void setPrecipitacao(double precipitacao) {
        this.precipitacao = precipitacao;
    }

    public String getCondicao() {
        return condicao;
    }

    public void setCondicao(String condicao) {
        this.condicao = condicao;
    }

    public double getVentoVelocidade() {
        return ventoVelocidade;
    }

    public void setVentoVelocidade(double ventoVelocidade) {
        this.ventoVelocidade = ventoVelocidade;
    }

    public String getVentoDirecao() {
        return ventoDirecao;
    }

    public void setVentoDirecao(String ventoDirecao) {
        this.ventoDirecao = ventoDirecao;
    }

}
